package com.security.calculator;

public class CalculatorCondition 
{
	public int addition(int a, int b)
	{
		int c = a + b;
		return c;
	}
	
	public int subtraction(int a, int b)
	{
		return a - b;
	}
	
	public int multiply(int a, int b)
	{
		return a * b;
	}
	
	public int divide(int a, int b)
	{
		return a / b;
	}
}
